//1. Показати прізвище та імя клієнта, назву послуги та дату початку полісу за 1 квартал 2022 р. Відсорт за ID полісу;
SELECT CUSTOMER.LASTNAME, CUSTOMER.FIRSTNAME, SERVICE.PRODUCT_NAME, POLICY.DATE_START
FROM POLICY, CUSTOMER, SERVICE
WHERE CUSTOMER_ID=CUSTOMER.ID 
AND SERVICE_ID=SERVICE.ID
AND DATE_START BETWEEN '2022/01/30' AND '2022/04/01'
ORDER BY POLICY.ID;

//2. Показати ID полісу, прізвище та імя клієнта, назву послуги та суму, яку потрібно сплатити за замовлення. Відсорт за ID полісу;
SELECT POLICY.ID, CUSTOMER.LASTNAME, CUSTOMER.FIRSTNAME, SERVICE.PRODUCT_NAME, (SERVICE.PRICE+IFNULL(POLICY.OVERPAY, 0))*IFNULL(100-CUSTOMER.DISCOUNT, 100)/100 AS COST
FROM POLICY, CUSTOMER, SERVICE
WHERE CUSTOMER_ID=CUSTOMER.ID 
AND SERVICE_ID=SERVICE.ID
GROUP BY POLICY.ID
ORDER BY POLICY.ID;

//3. Показати прізвище та імя страхових агентів та кількість замовлень, які вони виконали. Відсорт за ID агента; 
SELECT EMPLOYEE.LASTNAME, EMPLOYEE.FIRSTNAME, COUNT(*) AS NUMBER
FROM EMPLOYEE, POLICY
WHERE EMPLOYEE_ID=EMPLOYEE.ID
GROUP BY EMPLOYEE.ID
ORDER BY EMPLOYEE.ID;

