CREATE TABLE DEPARTMENT(ID INT PRIMARY KEY NOT NULL, NAME VARCHAR(20), CITY VARCHAR(15));
CREATE TABLE EMPLOYEE(ID INT PRIMARY KEY NOT NULL, FIRSTNAME VARCHAR(30), LASTNAME VARCHAR(30), GENDER VARCHAR(1), PHONE_NUMBERS INT, POSITION VARCHAR(15), DATE_EMPLOYMENT DATE, ID_DEPARTMENT INT, RATE INT, BONUS INT);
CREATE TABLE CUSTOMER(ID INT PRIMARY KEY NOT NULL, FIRSTNAME VARCHAR(30),LASTNAME VARCHAR(30), GENDER VARCHAR(1), DATE_BIRTH DATE, PHONE_NUMBERS INT, EMAIL VARCHAR(30), DISCOUNT INT);
CREATE TABLE BENEFICIAR(ID INT PRIMARY KEY NOT NULL, FIRSTNAME VARCHAR(30),LASTNAME VARCHAR(30), GENDER VARCHAR(1), DATE_BIRTH DATE, PHONE_NUMBERS INT, EMAIL VARCHAR(30));
CREATE TABLE SERVICE( ID INT PRIMARY KEY NOT NULL, PRODUCT_NAME VARCHAR(30), PRODUCT_DESCRIPTION VARCHAR(30), PRODUCT_TYPE VARCHAR(15), PRICE FLOAT); 
CREATE TABLE POLICY(ID INT PRIMARY KEY NOT NULL, NUMBER INT, SERVICE_ID INT, EMPLOYEE_ID INT, CUSTOMER_ID INT, BENEFICIAR_ID INT, INSURANCASE_ID INT, TRANSACTION_TYPE VARCHAR(15), DATE_START DATE,DATE_END DATE, AMOUNT INT);
CREATE TABLE INSURANCASE(ID INT PRIMARY KEY NOT NULL, NAME VARCHAR(20), PAYMENT_PERCENT INT);
CREATE TABLE PAYMENT(ID INT PRIMARY KEY NOT NULL, INSURANCASE_ID INT, POLICY_ID INT, DATE_INSURANCASE DATE, DATE_PAYMENT DATE);

